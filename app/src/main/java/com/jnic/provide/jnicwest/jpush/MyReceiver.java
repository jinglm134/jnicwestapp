    public void clickedMessage(Bundle bundle) {
//        String result = bundle.getString(JPushInterface.EXTRA_EXTRA);
//        String msg_id = bundle.getString(JPushInterface.EXTRA_MSG_ID);


        /** 前台是否运行 */
        boolean isFrontAppRuning = isRunningForeground();
        /** 后台是否运行 */
        boolean isBgAppRuning = isRunningBackground();

        // 不在前台
        if (!isFrontAppRuning) {
            Intent intent = new Intent(mContext, ActivitySplash.class);
            intent.addFlags(Intent.FLAG_ACTIVITY_NEW_TASK);
            if (isBgAppRuning) {
                intent.putExtra("type", isBgAppRuning);
            }
            mContext.startActivity(intent);
        }

    }

    private boolean isRunningForeground() {
        ActivityManager am = (ActivityManager) mContext.getSystemService(Context.ACTIVITY_SERVICE);
        ComponentName cn = am.getRunningTasks(1).get(0).topActivity;
        String currentPackageName = cn.getPackageName();
        if (!TextUtils.isEmpty(currentPackageName) && currentPackageName.equals(mContext.getPackageName())) {
            return true;
        }

        return false;
    }

    private boolean isRunningBackground() {
        ActivityManager am = (ActivityManager) mContext.getSystemService(Context.ACTIVITY_SERVICE);
        List<ActivityManager.RunningTaskInfo> list = am.getRunningTasks(100);
        for (ActivityManager.RunningTaskInfo info : list) {
            if (info.topActivity.getPackageName().equals(MY_PKG_NAME) && info.baseActivity.getPackageName().equals(MY_PKG_NAME)) {
                return true;
            }
        }
        return false;
    }
